// --- DetectSharedSourceLinks ---

func TestDetectSharedSourceLinks_TwoShared(t *testing.T) {
	linker := newLinker(t)

	pageA := linkerPage("id-a", "Page A", "content a")
	pageB := linkerPage("id-b", "Page B", "content b")

	refsA := []db.WikiPageRef{
		{PageID: "id-a", SourceType: "event", SourceID: "evt-1"},
		{PageID: "id-a", SourceType: "event", SourceID: "evt-2"},
		{PageID: "id-a", SourceType: "artifact", SourceID: "art-1"},
	}
	refsB := []db.WikiPageRef{
		{PageID: "id-b", SourceType: "event", SourceID: "evt-1"},
		{PageID: "id-b", SourceType: "event", SourceID: "evt-2"},
	}

	link := linker.DetectSharedSourceLinks(pageA, refsA, pageB, refsB)

	if link == nil {
		t.Fatal("expected a link for 2 shared sources, got nil")
	}
	if link.FromPageID != "id-a" {
		t.Errorf("FromPageID: want id-a, got %q", link.FromPageID)
	}
	if link.ToPageID != "id-b" {
		t.Errorf("ToPageID: want id-b, got %q", link.ToPageID)
	}
	if link.LinkType != "related" {
		t.Errorf("LinkType: want related, got %q", link.LinkType)
	}
	// 2 shared * 0.2 = 0.4, not capped.
	const wantStrength = 0.4
	if link.Strength != wantStrength {
		t.Errorf("Strength: want %f, got %f", wantStrength, link.Strength)
	}
}

