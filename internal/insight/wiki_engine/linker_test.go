// --- SaveDetectedLinks ---

func TestSaveDetectedLinks_Success(t *testing.T) {
	store := newMockStore()
	linker := wiki_engine.NewLinker(store)

	links := []db.WikiLink{
		{FromPageID: "id-a", ToPageID: "id-b", LinkType: "related", Strength: 0.5},
		{FromPageID: "id-b", ToPageID: "id-c", LinkType: "contradicts", Strength: 0.7},
	}

	count, err := linker.SaveDetectedLinks(links)
	if err != nil {
		t.Fatalf("SaveDetectedLinks: %v", err)
	}
	if count != 2 {
		t.Errorf("count: want 2, got %d", count)
	}
	// Verify links were sent to the store.
	fromALinks, _ := store.ListLinksFrom("id-a")
	_ = fromALinks // mockStore.ListLinksFrom returns nil; we check via ListLinksTo.
	toB, _ := store.ListLinksTo("id-b")
	if len(toB) != 1 {
		t.Errorf("expected 1 link pointing to id-b, got %d", len(toB))
	}
}

func TestSaveDetectedLinks_StoreError_ReturnsError(t *testing.T) {
	store := &linkerErrStore{saveErr: errors.New("db unavailable")}
	linker := wiki_engine.NewLinker(store)

	links := []db.WikiLink{
		{FromPageID: "id-a", ToPageID: "id-b", LinkType: "related", Strength: 0.5},
	}

	count, err := linker.SaveDetectedLinks(links)
	if err == nil {
		t.Fatal("expected error from store failure, got nil")
	}
	if count != 0 {
		t.Errorf("count: want 0 when all saves fail, got %d", count)
	}
}
